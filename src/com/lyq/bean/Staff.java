package com.lyq.bean;

public class Staff {
    private int sid;
    private  String sName;
    private int sBase;
    private int sAdd;
    private String sPassword;

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public String getsName() {
        return sName;
    }

    public void setsName(String sName) {
        this.sName = sName;
    }

    public int getsBase() {
        return sBase;
    }

    public void setsBase(int sBase) {
        this.sBase = sBase;
    }

    public int getsAdd() {
        return sAdd;
    }

    public void setsAdd(int sAdd) {
        this.sAdd = sAdd;
    }

    public String getsPassword() {
        return sPassword;
    }

    public void setsPassword(String sPassword) {
        this.sPassword = sPassword;
    }
}
